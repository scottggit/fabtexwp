<script>
  gsap.registerPlugin(ScrollTrigger);

    ScrollTrigger.defaults({
        toggleActions: "restart pause resume pause",
    });

    const sections = gsap.utils.toArray("section");

    let snap = ScrollTrigger.create({
        trigger: ".first",
        start: "top top+=60",
        endTrigger: ".last",
        end: "bottom bottom+=100",
        scrub: 1,
        onUpdate: (e) => {
            if (e.progress > 0.3 && e.progress < 0.4) {
                $('body').css({
                    'overflow': 'hidden'
                });
                window.addEventListener("wheel", scrollDetect);
            }
        },
        snap: {
            snapTo: 1 / (sections.length - 1),
            delay: 0.1,
            duration: {
                min: 0.2,
                max: 0.2
            }, // the snap animation should be at least 0.25 seconds, but no more than 0.75 seconds (determined by velocity)
            ease: "power1.inOut", // the ease of the snap animation ("power3" by default)
        },
    });

    function removeHandler() {
        window.removeEventListener("wheel", scrollDetect);
    }

    var count = 0;

    function scrollDetect(event) {
        // let section = document.querySelector('.fabric-chooser');
        let popSection = document.querySelector('.hotspot-pop');

        const delta = Math.sign(event.deltaY);

        if (delta == -1) {
            $('body').css({
                'overflow': 'scroll'
            });
            $('.hotspot-pop').addClass('invisible');
            $('.hotspot-pop').css({
                'left': '-901px'
            })
            removeHandler();
        }

        if (delta == 1) {
            $('.hotspot-pop').removeClass('invisible');
            $('.hotspot-pop').css({
                'left': '1px'
            });

        }

        if (ScrollTrigger.isInViewport(popSection)) {
            setTimeout(() => {
                $('body').css({
                    'overflow': 'scroll'
                });
                removeHandler();
            }, 1000)
        }

    }

    ScrollTrigger.batch(".our_clients .row>div .lg-n-container", {
        onEnter: (elements) => {
            gsap.from(elements, {
                y: 200,
                stagger: 0.25,
                ease: "Power2.Out",
                yoyo: true,
            });
        },
        once: false,
    });

    ScrollTrigger.batch(".c-card", {
        onEnter: (elements) => {
            gsap.from(elements, {
                y: 300,
                stagger: 0.15,
                ease: "Power2.Out",
                start: "top 90%",
                end: "top 40%",
                yoyo: true,
            });
        },
        once: false,
    });

    gsap.utils.toArray(".contact-form-wrap").forEach((elem) => {
        let tl = gsap
            .timeline({
                scrollTrigger: {
                    trigger: elem,

                    yoyo: true,
                },
            })
            .from(elem, {
                y: 100,
                duration: 1,
                ease: "Power2.Out"
            });
    });

    gsap.utils.toArray(".newsletter-wrap").forEach((elem) => {
        let tl = gsap
            .timeline({
                scrollTrigger: {
                    trigger: elem,

                    yoyo: true,
                },
            })
            .from(elem, {
                width: 0,

                duration: 1,
                ease: "Power2.Out",
            });
    });

    $(".floating-line1").each(function() {
        const svg = gsap.timeline({
            scrollTrigger: {
                trigger: $(this),
                start: "top 80%",
                end: "bottom 100%", // end after scrolling 500px beyond the start
